
public class OperadoresAsigancion {
    public static void main(String[] args) {
        int a=2 , b=3;
        int c = a + 5 - b;
        System.out.println("c = " + c);
        
        a +=1;
        System.out.println("a = " + a);
        
        a += 3;
        System.out.println("a = " + a);
        
        a -= 2; // a = a - 2
        System.out.println("a = " + a);
        
        
    }
 
}
